var jam = setInterval(function(){
  var time = new Date();
  
  var hour = time.getHours();
  var minute = time.getMinutes();
  var second = time.getSeconds();
  
  document.getElementById("jam").innerHTML = hour;
  document.getElementById("menit").innerHTML = minute;
  document.getElementById("detik").innerHTML = second;
  
  if (hour >= 1 && hour <= 10){
    document.getElementById("selamat").innerHTML = "Selamat Pagi";
  } else if (hour >= 10 && hour <= 15){
    document.getElementById("selamat").innerHTML = "Selamat Siang";
  } else if (hour <= 15 && hour <= 18){
    document.getElementById("selamat").innerHTML = "Selamat Sore";
  } else if (hour <= 15 || hour || 1){
    document.getElementById("selamat").innerHTML = "Selamat Malam";
  } else {
    document.getElementById("selamat").innerHTML = "Tengah Malam"
  }
  
},1000);